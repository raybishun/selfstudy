# =============================================================================
# While Loop
# =============================================================================
i = 0
while(i < 5):
    print(i)
    i = i + 1
else :
    print("the end...")

# =============================================================================
# For Loop
# =============================================================================
nums = ["one", "two", "three", "four", "five"]
for num in nums:
    print(num)

# =============================================================================
# For Loop using range
# =============================================================================
j = 10
for j in range(0,8):
    print(j)