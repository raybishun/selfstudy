# =============================================================================
# Numeric Operators
# =============================================================================

# If one of the operands is a float, a float is returned
print(2 + 1)
print(2.0 + 1)

# Division (new to Python 3.X, returns a float)
# aka 'regular division'
print(5 / 3)

# Floor Division (only returns int)
print(5 // 3)

# Modulo (returns only the remainder)
print(5 % 3)

# Exponents
print(2 ** 3) # 2 x 2 x 2 = 8