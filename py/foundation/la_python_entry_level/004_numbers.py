# Scientific notation
print(f'Exponent to the power of 10: {4.5e9}') # note lower case e
print(f'Exponent to the power of 10: {4.5E9}') # note upper case E
print(f'4.5e9 equals {4.5 * (10 ** 9)}') # where ** is 'power'
print(f'{4.5e9 == 4.5 * (10 ** 9)}')