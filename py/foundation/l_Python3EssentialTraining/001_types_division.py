# =============================================================================
# Division and modulo
# =============================================================================
print('\x1b[2J')

# Division
x = 7 / 3
print(f'{x}')
print(type(x))
print()

# // returns only the int
x = 7 // 3 
print(f'{x}')
print(type(x))
print()

# modulo (the %) returns remainder
x = 7 % 3
print(f'{x}')
print(type(x))
print()