# =============================================================================
# Numeric Operators
# =============================================================================

# If one of the operands is a float, a float is returned
print(2 + 1)
# returns --> 3

print(2.0 + 1)
# returns --> 3.0

# Division, aka 'regular division' (new to Python 3.X, returns a float)
print(5 / 3)
# returns --> 1.6666666666666667

# Floor Division, only returns a whole number (int)
print(5 // 3)
# returns --> 1

# Modulo (returns only the remainder)
print(5 % 3)
# returns --> 2

# Power
print(2 ** 3) # 2 x 2 x 2 = 8 (in math it's 2 ^ 3)
# returns --> 8