# =============================================================================
# What returns false?
# =============================================================================

# -----------------------------------------------------------------------------
# None returns false
# -----------------------------------------------------------------------------
x = None
print(f'{x}')
print(type(x))

if x: 
    print("True")
else:
    print("False")
print()

# -----------------------------------------------------------------------------
# 0 returns false
# -----------------------------------------------------------------------------
x = 0
print(f'{x}')
print(type(x))

if x: 
    print("True")
else:
    print("False")
print()

# -----------------------------------------------------------------------------
# Empty string returns false
# -----------------------------------------------------------------------------
x = ""
print(f'{x}')
print(type(x))

if x: 
    print("True")
else:
    print("False")
print()