# Floor division (how many times 3 divides into 5)
print( 5 // 3 ) # 1
print( 6 // 2 ) # 3

# Modulo (return the remainder)
# Modulo is commonly used to determine if some value is even or odd
print (  5 % 3  ) # 2
print( 6 % 2 ) # 0 (even)
print ( 7 % 2 ) # 1 (odd)

# Exponents ( ** )
print( 2 ** 3 ) # 8 (2 * 2 * 2 = 8), aka 2 to the power of 3