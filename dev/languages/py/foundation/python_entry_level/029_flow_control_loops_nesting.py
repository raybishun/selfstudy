# Flow Control: Loops: Nesting
# =============================================================================

# Get even numbers
ctr = 1
while ctr <= 10:
    if ctr % 2 == 0:
        print(ctr)
    ctr += 1
print()

# Get odd numbers
ctr = 1
while ctr <= 10:
    if ctr % 2 != 0:
        print(ctr)
    ctr += 1
print()